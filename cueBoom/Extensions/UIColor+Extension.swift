import Foundation
import UIKit

extension UIColor {
    var coreImageColor: CIColor {
        return CIColor(color: self)
    }
    
    convenience init(red: Int, green: Int, blue: Int) {
        
        self.init(red: CGFloat(red) / 255.0, green: CGFloat(green) / 255.0, blue: CGFloat(blue) / 255.0, alpha: 1.0)
    }
    
    convenience init(rgb: Int) {
        self.init(
            red: (rgb >> 16) & 0xFF,
            green: (rgb >> 8) & 0xFF,
            blue: rgb & 0xFF
        )
    }
    
    func distanceFrom(_ color: UIColor) -> CGFloat? {
        guard let color1Components = self.cgColor.components, let color2Components = color.cgColor.components else {return nil}
        


        //components[0] is red
        //components[1] is green
        //components[2] is blue
        //components[3] is alpha
        let diff = abs(color1Components[0] - color2Components[0]) + abs(color1Components[1] - color2Components[1]) + abs(color1Components[2] - color2Components[2]) + abs(color1Components[3] - color2Components[3])
        
        
        return diff
        
    }
    
